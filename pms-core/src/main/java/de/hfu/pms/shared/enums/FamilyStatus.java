package de.hfu.pms.shared.enums;

/**
 * FamilyStatus defines all possible family status types.
 */
public enum FamilyStatus {

    /**
     * A single person in this context is someone who is unmarried.
     */
    Single,

    /**
     * A person which is officially married to another person.
     */
    Married,

    /**
     * There is no information available about family status
     */
    NoInformation

}
